from __future__ import annotations

from pathlib import Path

import devtools.demo_env as demo_env
from devtools.live_testnet_support import DemoWalletSet
from xrpl.wallet import Wallet


BASE_ENV = """# Generated by quickstart
XRPL_RPC_URL=https://resolved.testnet.rpc/
NETWORK_ID=xrpl:1
XRPL_NETWORK=xrpl:1
MY_DESTINATION_ADDRESS=rMerchant
FACILITATOR_BEARER_TOKEN=top-secret-token
XRPL_WALLET_SEED=sEdSuperSecretSeed
PRICE_DROPS=1000
PRICE_ASSET_CODE=XRP
PRICE_ASSET_ISSUER=
PRICE_ASSET_AMOUNT=
PAYMENT_ASSET=XRP:native
ALLOWED_ISSUED_ASSETS=
"""


def build_demo_wallet_set() -> DemoWalletSet:
    return DemoWalletSet(
        merchant_wallet=Wallet.create(),
        buyers={
            "xrp": Wallet.create(),
            "rlusd": Wallet.create(),
            "usdc": Wallet.create(),
        },
    )


def stub_demo_wallet_cache(monkeypatch, tmp_path: Path) -> DemoWalletSet:
    wallet_set = build_demo_wallet_set()
    monkeypatch.setattr(demo_env, "load_cached_demo_wallet_set", lambda _cache_path=None: wallet_set)
    monkeypatch.setattr(demo_env, "wallet_cache_path", lambda: tmp_path / "wallet-cache.json")
    return wallet_set


def test_demo_env_writes_rlusd_variant_with_extra_allowed_asset(
    monkeypatch,
    tmp_path: Path,
    capsys,
) -> None:
    wallet_set = stub_demo_wallet_cache(monkeypatch, tmp_path)
    base_path = tmp_path / ".env.quickstart"
    output_path = tmp_path / ".env.quickstart.rlusd"
    base_path.write_text(BASE_ENV, encoding="utf-8")
    monkeypatch.setattr(demo_env, "default_rlusd_issuer", lambda: "rCustomRLUSDIssuer")

    exit_code = demo_env.main(
        [
            "--asset",
            "rlusd",
            "--base",
            str(base_path),
            "--output",
            str(output_path),
        ]
    )
    captured = capsys.readouterr()

    rendered = output_path.read_text(encoding="utf-8")
    assert exit_code == 0
    assert "PRICE_ASSET_CODE=RLUSD" in rendered
    assert "PRICE_ASSET_ISSUER=rCustomRLUSDIssuer" in rendered
    assert "PRICE_ASSET_AMOUNT=1.25" in rendered
    assert "PAYMENT_ASSET=RLUSD:rCustomRLUSDIssuer" in rendered
    assert "ALLOWED_ISSUED_ASSETS=RLUSD:rCustomRLUSDIssuer" in rendered
    assert f"XRPL_WALLET_SEED={wallet_set.buyer_wallet('rlusd').seed}" in rendered
    assert wallet_set.buyer_wallet("xrp").seed not in rendered
    assert f"Buyer address: {wallet_set.buyer_wallet('rlusd').classic_address}" in captured.out
    assert "Buyer payment asset: RLUSD:rCustomRLUSDIssuer" in captured.out
    assert "Facilitator extra issued assets: RLUSD:rCustomRLUSDIssuer" in captured.out


