def test_demo_env_variants_use_distinct_asset_specific_buyer_wallets(
    monkeypatch,
    tmp_path: Path,
) -> None:
    wallet_set = stub_demo_wallet_cache(monkeypatch, tmp_path)
    base_path = tmp_path / ".env.quickstart"
    base_path.write_text(BASE_ENV, encoding="utf-8")
    monkeypatch.setattr(demo_env, "default_rlusd_issuer", lambda: "rCustomRLUSDIssuer")
    monkeypatch.setattr(demo_env, "default_usdc_issuer", lambda: "rHuGNhqTG32mfmAvWA8hUyWRLV3tCSwKQt")

    for asset in ("xrp", "rlusd", "usdc"):
        exit_code = demo_env.main(
            [
                "--asset",
                asset,
                "--base",
                str(base_path),
                "--output",
                str(tmp_path / f".env.quickstart.{asset}"),
            ]
        )
        assert exit_code == 0

    rendered_by_asset = {
        asset: (tmp_path / f".env.quickstart.{asset}").read_text(encoding="utf-8")
        for asset in ("xrp", "rlusd", "usdc")
    }
    seeds = {
        asset: demo_env.get_env_value(
            demo_env.parse_env_lines(rendered_by_asset[asset]),
            "XRPL_WALLET_SEED",
        )
        for asset in rendered_by_asset
    }

    assert seeds == {
        "xrp": wallet_set.buyer_wallet("xrp").seed,
        "rlusd": wallet_set.buyer_wallet("rlusd").seed,
        "usdc": wallet_set.buyer_wallet("usdc").seed,
    }
    assert len(set(seeds.values())) == 3


def test_demo_env_requires_v2_demo_wallet_cache(monkeypatch, tmp_path: Path) -> None:
    base_path = tmp_path / ".env.quickstart"
    base_path.write_text(BASE_ENV, encoding="utf-8")
    monkeypatch.setattr(demo_env, "load_cached_demo_wallet_set", lambda _cache_path=None: None)
    monkeypatch.setattr(demo_env, "wallet_cache_path", lambda: tmp_path / "wallet-cache.json")

    try:
        demo_env.main(["--asset", "rlusd", "--base", str(base_path)])
    except RuntimeError as exc:
        assert "Rerun `python -m devtools.quickstart` first." in str(exc)
    else:
        raise AssertionError("Expected demo_env.main to fail when the wallet cache is missing")
